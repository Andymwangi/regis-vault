/**
 * Generates user initials from a name, taking the first letter of the first and last name
 * @param fullName User's full name
 * @param email User's email as fallback
 * @returns Initials (2 characters if possible)
 */
export function getUserInitials(fullName?: string | null, email?: string | null): string {
  // If no fullName is provided, check email
  if (!fullName || fullName.trim() === '') {
    if (email) {
      // Use first two letters of email, or just first if email is only one character
      const username = email.split('@')[0];
      return username.length > 1 
        ? (username[0] + username[1]).toUpperCase() 
        : username[0].toUpperCase();
    }
    return 'U';
  }
  
  // Split name and filter out empty parts
  const nameParts = fullName.trim().split(' ').filter(Boolean);
  
  // If we have at least 2 parts, take first letter of first and last parts
  if (nameParts.length >= 2) {
    return (nameParts[0][0] + nameParts[nameParts.length - 1][0]).toUpperCase();
  }
  
  // If only one part, take first letter and second letter if available
  return nameParts[0].length > 1 
    ? (nameParts[0][0] + nameParts[0][1]).toUpperCase() 
    : nameParts[0][0].toUpperCase();
}

/**
 * Gets the user's first name from their full name or email
 * @param fullName User's full name
 * @param email User's email as fallback
 * @returns The user's first name
 */
export function getUserFirstName(fullName?: string | null, email?: string | null): string {
  // If user has a name, use the first part as the first name
  if (fullName) {
    const nameParts = fullName.trim().split(' ').filter(Boolean);
    return nameParts[0];
  }
  
  // Fall back to email (before the @ symbol)
  if (email) {
    return email.split('@')[0];
  }
  
  return 'User';
}
